public class Doubling {
  private int recieved;
  private int result;

  public Doubling() {
    this.recieved = recieved;
    this.result = result;
  }

  public int getRecieved() {
    return recieved;
  }

  public void setRecieved(int recieved) {
    this.recieved = recieved;
  }

  public int getResult() {
    return result;
  }

  public void setResult(int result) {
    this.result = result;
  }
}
